    CONSTRAINT [IMCK_SalesOrderHeader_DueDate] CHECK ([DueDate]>=[OrderDate]),
    CONSTRAINT [IMCK_SalesOrderHeader_Freight] CHECK ([Freight]>=(0.00)),
